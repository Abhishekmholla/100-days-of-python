from math import ceil
import random

# To generate a random integer number
random_number = random.randint(1,10)
print(random_number)

# To generate a random floating point number b/w 0 and 1
random_number = random.random()
print(random_number)

# To generate a random floating point number b/w 0 and 5
random_number = round(random.random() * 5)
print(random_number)

