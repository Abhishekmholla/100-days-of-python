fruits = ['Apple','Peach','Pear']

# Prints all fruits taking one at time in loop
for fruit in fruits:
    print(fruit)
    print(fruit + " Pie")

# Prints number in the range 1 to 10
print("Numbers in range 1-10")
for number in range(1,11):
    print(number)
    
print("Numbers in range 1-10 with step as 2")
# Prints number in the range 1 to 10 with a step 2
for number in range(1,11,2):
    print(number)